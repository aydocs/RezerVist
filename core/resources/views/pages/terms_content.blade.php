<div class="space-y-6">
    <div>
        <h4 class="font-black text-gray-900 mb-2">1. Taraflar ve Tanımlar</h4>
        <p>Bu sözleşme, Rezervist (bundan böyle "PLATFORM" olarak anılacaktır) ile bu platformu kullanan kullanıcı (bundan böyle "KULLANICI" olarak anılacaktır) arasında akdedilmiştir. PLATFORM, restoran ve etkinlik rezervasyon hizmetleri sunan bir aracı hizmet sağlayıcıdır.</p>
    </div>

    <div>
        <h4 class="font-black text-gray-900 mb-2">2. Hizmetin Kapsamı</h4>
        <p>Rezervist, kullanıcıların anlaşmalı işletmelerde rezervasyon yapmasını, işletmeleri incelemesini ve puanlamasını sağlayan bir dijital platformdur. Rezervist, hizmet sağlayıcı (restoran vb.) değildir; yalnızca kullanıcı ile işletme arasında rezervasyon köprüsü kurar.</p>
    </div>

    <div>
        <h4 class="font-black text-gray-900 mb-2">3. Kullanıcı Yükümlülükleri</h4>
        <ul class="list-disc pl-5 space-y-2">
            <li>Kullanıcı, platforma üye olurken verdiği bilgilerin doğru olduğunu taahhüt eder.</li>
            <li>Kullanıcı, oluşturduğu rezervasyonlara gitmeyeceğini (No-Show) en az 2 saat önceden bildirmekle yükümlüdür.</li>
            <li>Kullanıcı, platform üzerinde genel ahlaka aykırı, yasa dışı veya rahatsız edici içerik paylaşamaz.</li>
            <li>Kullanıcı hesabının güvenliğinden bizzat kullanıcı sorumludur.</li>
        </ul>
    </div>

    <div>
        <h4 class="font-black text-gray-900 mb-2">5. Ödeme ve Komisyon Şartları</h4>
        <p>PLATFORM üzerinde gerçekleşen tüm ön ödemeli rezervasyonlar için aşağıdaki finansal koşullar geçerlidir:</p>
        <ul class="list-disc pl-5 mt-2 space-y-2">
            <li><strong>Platform Hizmet Bedeli:</strong> Yapılan her satış tutarı üzerinden %5 (yüzde beş) oranında platform hizmet bedeli kesilmektedir.</li>
            <li><strong>Ödeme Altyapısı:</strong> Ödemeler Iyzico Marketplace (Pazaryeri) altyapısı ile güvence altına alınmıştır.</li>
            <li><strong>Hakediş Aktarımı:</strong> Satış tutarı, Platform hizmet bedeli ve ödeme kuruluşu işlem ücretleri düşüldükten sonra kalan net tutar, Iyzico tarafından doğrudan işletmenin tanımlı banka hesabına aktarılır.</li>
            <li><strong>Şeffaflık:</strong> İşletme, tüm kesinti kalemlerini ve net kazanç detaylarını İşletme Paneli > Finansal Raporlar sekmesinden takip edebilir.</li>
        </ul>
    </div>

    <div>
        <h4 class="font-black text-gray-900 mb-2">6. Gizlilik ve Kişisel Veriler</h4>
