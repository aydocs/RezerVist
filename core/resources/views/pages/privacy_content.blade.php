<p class="text-gray-600 mb-8 leading-relaxed">
    Rezervist olarak kişisel verilerinizin güvenliğine ve gizliliğine önem veriyoruz. Bu politika, 6698 sayılı Kişisel Verilerin Korunması Kanunu ("KVKK") kapsamında verilerinizin nasıl işlendiğini açıklar.
</p>

<div class="space-y-6">
    <div>
        <h4 class="font-black text-gray-900 mb-2">1. Veri Sorumlusu</h4>
        <p>Rezervist, KVKK kapsamında "Veri Sorumlusu" sıfatıyla kişisel verilerinizi işlemektedir.</p>
    </div>

    <div>
        <h4 class="font-black text-gray-900 mb-2">2. İşlenen Kişisel Verileriniz</h4>
        <ul class="list-disc pl-5 space-y-2">
            <li><strong>Kimlik Bilgileri:</strong> Ad, soyad.</li>
            <li><strong>İletişim Bilgileri:</strong> E-posta adresi, telefon numarası.</li>
            <li><strong>İşlem Bilgileri:</strong> Rezervasyon geçmişi, favoriler, yorumlar.</li>
            <li><strong>İşlem Güvenliği Bilgileri:</strong> IP adresi, log kayıtları, cihaz bilgileri.</li>
        </ul>
    </div>

    <div>
        <h4 class="font-black text-gray-900 mb-2">3. Kişisel Verilerin İşlenme Amaçları</h4>
        <p>Kişisel verileriniz aşağıdaki amaçlarla işlenmektedir:</p>
        <ul class="list-disc pl-5 space-y-2">
            <li>Hizmetlerin sunulması ve rezervasyon süreçlerinin yürütülmesi.</li>
            <li>Müşteri destek süreçlerinin yönetilmesi.</li>
            <li>Yasal yükümlülüklerin yerine getirilmesi.</li>
            <li>Hizmet kalitesinin artırılması ve analiz çalışmaları.</li>
        </ul>
    </div>

    <div>
        <h4 class="font-black text-gray-900 mb-2">4. Kişisel Verilerin Aktarılması</h4>
        <p>Kişisel verileriniz, yasal zorunluluklar dışında veya açık rızanız olmaksızın üçüncü kişilerle paylaşılmamaktadır. Paylaşılan istisnai taraflar şunlardır:</p>
        <ul class="list-disc pl-5 mt-2 space-y-2">
            <li><strong>İşletmeler:</strong> Rezervasyonun gerçekleşmesi için gerekli olan ad, soyad ve telefon bilgisi.</li>
            <li><strong>Ödeme Kuruluşu (Iyzico):</strong> Ödemelerin güvenli bir şekilde işlenmesi ve hakedişlerin dağıtımı için gerekli olan finansal işlem ve kimlik verileri.</li>
        </ul>
    </div>

    <div>
        <h4 class="font-black text-gray-900 mb-2">5. Kişisel Veri Toplama Yöntemi ve Hukuki Sebebi</h4>
        <p>Verileriniz, internet sitemiz ve mobil uygulamamız üzerinden elektronik ortamda toplanmaktadır. Hukuki sebeplerimiz: KVKK Md. 5/2-c (Sözleşmenin kurulması) ve Md. 5/2-ç (Veri sorumlusunun hukuki yükümlülüğü).</p>
    </div>

    <div>
        <h4 class="font-black text-gray-900 mb-2">6. İlgili Kişinin Hakları (KVKK Madde 11)</h4>
        <p>KVKK'nın 11. maddesi uyarınca herkes veri sorumlusuna başvurarak kendisiyle ilgili;</p>
        <ul class="list-disc pl-5 space-y-2">
            <li>Kişisel veri işlenip işlenmediğini öğrenme,</li>
            <li>Kişisel verileri işlenmişse buna ilişkin bilgi talep etme,</li>
            <li>Kişisel verilerin işlenme amacını ve bunların amacına uygun kullanılıp kullanılmadığını öğrenme,</li>
            <li>Yurt içinde veya yurt dışıda kişisel verilerin aktarıldığı üçüncü kişileri bilme haklarına sahiptir.</li>
        </ul>
    </div>

    <div>
        <h4 class="font-black text-gray-900 mb-2">7. Çerez (Cookie) Politikası</h4>
        <p>Sitemizde kullanıcı deneyimini iyileştirmek için çerezler kullanılmaktadır. Tarayıcı ayarlarınızdan çerezleri dilediğiniz zaman engelleyebilirsiniz.</p>
    </div>

    <div>
        <h4 class="font-black text-gray-900 mb-2">8. Veri Güvenliği</h4>
        <p>Kişisel verileriniz, yetkisiz erişime, kaybolmaya veya ifşaya karşı SSL şifreleme ve güvenli sunucu altyapıları ile korunmaktadır.</p>
    </div>
</div>

